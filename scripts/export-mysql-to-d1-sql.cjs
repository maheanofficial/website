#!/usr/bin/env node
/* eslint-disable no-console */
const fs = require('node:fs/promises');
const path = require('node:path');
const mysql = require('mysql2/promise');

const ROOT_DIR = path.resolve(__dirname, '..');
const DEFAULT_OUT_PATH = path.join(ROOT_DIR, 'tmp', 'd1-seed.sql');

const pickEnv = (...keys) => {
    for (const key of keys) {
        const value = process.env[key];
        if (typeof value === 'string' && value.trim()) {
            return value.trim();
        }
    }
    return '';
};

const parseArgs = (argv) => {
    const out = {};
    for (let i = 0; i < argv.length; i += 1) {
        const token = argv[i];
        if (token === '--out' && argv[i + 1]) {
            out.out = argv[i + 1];
            i += 1;
        }
    }
    return out;
};

const normalizeInt = (value, fallback) => {
    const parsed = Number.parseInt(String(value || ''), 10);
    return Number.isFinite(parsed) && parsed > 0 ? parsed : fallback;
};

const escapeSqlValue = (value) =>
    `'${String(value ?? '').replace(/'/g, "''")}'`;

const parseJsonRecord = (value, fallback = {}) => {
    try {
        const parsed = JSON.parse(String(value || '{}'));
        if (parsed && typeof parsed === 'object' && !Array.isArray(parsed)) {
            return parsed;
        }
    } catch {
        // Ignore parse errors.
    }
    return fallback;
};

const toUserRowJson = (row) => {
    const parsed = parseJsonRecord(row?.user_json, null);
    if (parsed) {
        return parsed;
    }

    const providers = (() => {
        try {
            const raw = JSON.parse(String(row?.providers_json || '[]'));
            return Array.isArray(raw) ? raw : [];
        } catch {
            return [];
        }
    })();

    return {
        id: String(row?.id || ''),
        username: String(row?.username || ''),
        email: row?.email ? String(row.email) : null,
        password: String(row?.password || ''),
        role: String(row?.role || 'reader'),
        createdAt: String(row?.created_at || new Date().toISOString()),
        displayName: String(row?.display_name || ''),
        photoURL: row?.photo_url ? String(row.photo_url) : null,
        providers
    };
};

const loadDeployLocalJson = async () => {
    const filePath = path.join(ROOT_DIR, '.deploy.local.json');
    try {
        const raw = await fs.readFile(filePath, 'utf8');
        const parsed = JSON.parse(raw);
        return parsed && typeof parsed === 'object' ? parsed : {};
    } catch {
        return {};
    }
};

const main = async () => {
    const args = parseArgs(process.argv.slice(2));
    const deployConfig = await loadDeployLocalJson();

    const mysqlHost = pickEnv('MYSQL_HOST', 'DB_HOST') || String(deployConfig.host || '');
    const mysqlPort = normalizeInt(pickEnv('MYSQL_PORT', 'DB_PORT'), 3306);
    const mysqlUser = pickEnv('MYSQL_USER', 'DB_USER') || String(deployConfig.user || '');
    const mysqlPassword = pickEnv('MYSQL_PASSWORD', 'DB_PASSWORD') || String(deployConfig.password || '');
    const mysqlDatabase = pickEnv('MYSQL_DATABASE', 'DB_NAME') || 'mahean_site';
    const mysqlTablePrefix = pickEnv('MYSQL_TABLE_PREFIX', 'DB_TABLE_PREFIX') || 'app_table';
    const mysqlUsersTable = pickEnv('MYSQL_USERS_TABLE', 'DB_USERS_TABLE') || 'app_users';
    const outputPath = path.resolve(ROOT_DIR, args.out || DEFAULT_OUT_PATH);

    if (!mysqlHost || !mysqlUser || !mysqlDatabase) {
        throw new Error('Missing MySQL credentials. Set MYSQL_HOST, MYSQL_USER, MYSQL_DATABASE.');
    }

    const connection = await mysql.createConnection({
        host: mysqlHost,
        port: mysqlPort,
        user: mysqlUser,
        password: mysqlPassword,
        database: mysqlDatabase,
        charset: 'utf8mb4'
    });

    try {
        const [tableRows] = await connection.query('SHOW TABLES');
        const allTableNames = (Array.isArray(tableRows) ? tableRows : [])
            .map((entry) => String(Object.values(entry || {})[0] || ''))
            .filter(Boolean);
        const dataTablePrefix = `${mysqlTablePrefix}_`;
        const logicalTables = allTableNames
            .filter((tableName) => tableName.startsWith(dataTablePrefix))
            .map((tableName) => ({
                physicalName: tableName,
                logicalName: tableName.slice(dataTablePrefix.length)
            }));

        const sqlLines = [];
        sqlLines.push('-- Auto-generated by scripts/export-mysql-to-d1-sql.cjs');
        sqlLines.push(`
CREATE TABLE IF NOT EXISTS "app_table_rows" (
  "pk" INTEGER PRIMARY KEY AUTOINCREMENT,
  "table_name" TEXT NOT NULL,
  "row_json" TEXT NOT NULL,
  "created_at" TEXT NOT NULL,
  "updated_at" TEXT NOT NULL
);`.trim());
        sqlLines.push('CREATE INDEX IF NOT EXISTS "idx_app_table_rows_table_name" ON "app_table_rows"("table_name");');
        sqlLines.push('DELETE FROM "app_table_rows";');

        let insertedRows = 0;
        const now = new Date().toISOString();

        for (const table of logicalTables) {
            const [rows] = await connection.query(
                `SELECT row_json FROM \`${table.physicalName}\` ORDER BY pk ASC`
            );
            for (const row of Array.isArray(rows) ? rows : []) {
                const rowJson = String(row?.row_json || '{}');
                sqlLines.push(
                    `INSERT INTO "app_table_rows" ("table_name","row_json","created_at","updated_at") VALUES (${escapeSqlValue(table.logicalName)},${escapeSqlValue(rowJson)},${escapeSqlValue(now)},${escapeSqlValue(now)});`
                );
                insertedRows += 1;
            }
            console.log(`[export] ${table.physicalName} -> ${table.logicalName} rows=${rows.length}`);
        }

        if (allTableNames.includes(mysqlUsersTable)) {
            const [rows] = await connection.query(
                `SELECT id, username, email, password, role, created_at, display_name, photo_url, providers_json, user_json
                 FROM \`${mysqlUsersTable}\`
                 ORDER BY updated_at ASC, id ASC`
            );
            for (const row of Array.isArray(rows) ? rows : []) {
                const rowJson = JSON.stringify(toUserRowJson(row));
                sqlLines.push(
                    `INSERT INTO "app_table_rows" ("table_name","row_json","created_at","updated_at") VALUES (${escapeSqlValue('users')},${escapeSqlValue(rowJson)},${escapeSqlValue(now)},${escapeSqlValue(now)});`
                );
                insertedRows += 1;
            }
            console.log(`[export] ${mysqlUsersTable} -> users rows=${rows.length}`);
        } else {
            console.log(`[export] skipped users table; missing "${mysqlUsersTable}"`);
        }

        await fs.mkdir(path.dirname(outputPath), { recursive: true });
        await fs.writeFile(outputPath, `${sqlLines.join('\n')}\n`, 'utf8');
        console.log(`[export] wrote ${insertedRows} rows to ${outputPath}`);
    } finally {
        await connection.end().catch(() => undefined);
    }
};

main().catch((error) => {
    console.error('[export] failed:', error?.message || error);
    process.exitCode = 1;
});
